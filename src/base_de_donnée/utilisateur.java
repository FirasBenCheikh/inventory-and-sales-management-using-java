    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(txt_cher.getText().equals("")){ //si le textfield de la recherche est vide  ,le programme va afficher le message ci dessous
           JOptionPane.showMessageDialog(this, "Taper quelque chose !!");
       }else{
           if(box_cher.getSelectedItem().equals("id_user")){
               rs = db.querySelectAll("utilisateur","id_user LIKE '%" + txt_cher.getText() + "%' ");
               table_user.setModel(new ResultSetTableModel(rs));
           }else if (box_cher.getSelectedItem().equals("username")){
               rs = db.querySelectAll("utilisateur", "username LIKE '%" + txt_cher.getText() + "%' ");
               table_user.setModel(new ResultSetTableModel(rs));
           }else if (box_cher.getSelectedItem().equals("password")){
               rs = db.querySelectAll("utilisateur", "password LIKE '%" + txt_cher.getText() + "%' ");
               table_user.setModel(new ResultSetTableModel(rs));
           }else if (box_cher.getSelectedItem().equals("type")){
               rs = db.querySelectAll("utilisateur", "type LIKE '%" + txt_cher.getText() + "%' ");
               table_user.setModel(new ResultSetTableModel(rs));}
       }
    }//GEN-LAST:event_jButton1ActionPerformed

