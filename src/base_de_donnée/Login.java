    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //txt_username et txt_password sont les noms de champs design
        rs = db.querySelectAll("utilisateur", "username='" + txt_username.getText()+ "' and password='" + txt_password.getText() + "'" );
        try {
            while(rs.next()){
                username1 = rs.getString("username");
                password1 = rs.getString("password");
                hak = rs.getString("type");
            }
        } catch (SQLException ex) {
            
          Logger.getLogger(Login.class.getName()).log(Level.SEVERE,null,ex);
        }
        if (username1 == null && password1 == null){
            JOptionPane.showMessageDialog(this, "le nom utilisateur ou le mots de passe est incorrecte");
            jlabel.setText("erreur!! Veuiller essayer de nouveau ");
            jlabel.setVisible(true);
        }
        else{
            if(hak.equals("Directeur")){
                Principale h = new Principale();
                h.setVisible(true);
                this.dispose(); //si on a le directeur le programme va ouvrir la page principale
            }else{
                Cachier k = new Cachier(); // et si on a cachier le programme va ouvrir jframe cachier
                k.setVisible(true);
                //this.dispose(); //fonction dispose pour assurer la fermeture
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

